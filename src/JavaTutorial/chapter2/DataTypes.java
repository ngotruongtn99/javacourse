package JavaTutorial.chapter2;

public class DataTypes {
	public static void main(String[] args) {
		int myNum = 5; // Integer (whole number)
		float myFloatNum = 5.99f; // Floating point number
		char myLetter = 'D'; // Character
		boolean myBool = true; // Boolean
		String myText = "Hello"; // String

		/*
		 * Kiểu dữ liệu được chia thành 2 nhóm: - Primitive data types (dữ liệu nguyên
		 * thủy), bao gồm: byte, short, int, long, float, double, boolean, char -
		 * Non-primitive data types : String, Arrays and Classes (sẽ học sau)
		 */
	}
}
